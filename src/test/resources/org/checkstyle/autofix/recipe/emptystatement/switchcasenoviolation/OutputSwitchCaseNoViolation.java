package org.checkstyle.autofix.recipe.emptystatement.switchcasenoviolation;

public class OutputSwitchCaseNoViolation {
    public void normalSwitch(int value) {
        switch (value) {
            case 1:
                System.out.println("one");
                break;
            case 2:
                System.out.println("two");
                break;
            default:
                System.out.println("other");
                break;
        }
    }
}
